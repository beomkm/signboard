static ssize_t dev_write(struct file *fp, const char *buffer, size_t len, loff_t *offset)
{
	int i;
	int j;
	int bits = 0;

	printk(KERN_INFO "dev_write %d characters\n", len);
	printk(KERN_INFO "dev_write data : %s\n", buffer);

	//*((unsigned int*)0x20200000) = 1<<15;
	//*((unsigned int*)0x2020001c) = 1<<5;

	for(i=0; i<10; i++) {
		bits |= 1;
		bits = bits << 3;
	}

	for(i=0; i<10; i++)
		writel(bits, addr); //set gpio i output
	for(i=10; i<20; i++)
		writel(bits, (addr+0x4)); //set gpio i output
	for(i=20; i<30; i++)
		writel(bits, (addr+0x8)); //set gpio i output


	//lcd initial setting
	writel(1<<cs, (addr+0x1c));
	writel(1<<rst, (addr+0x28));
	writel(1<<rst, (addr+0x1c));

	//st7565 instructions
	send_data(0xe2, 0); //Reset

	send_data(0xa3, 0); //LCD bias voltage
	send_data(0xa0, 0); //Display flapped on/off
	send_data(0xc8, 0); //Output scan reverse on/off
	send_data(0xa4, 0); //Make all pixels on on/off
	send_data(0xa6, 0); //Display reverse on/off

	send_data(0x2f, 0); //Power control
	send_data(0x60, 0); //Set starting line num
	send_data(0x22, 0); //Internal resistor ratio
	send_data(0x81, 0); //Brighteness
	send_data(0x28, 0); //Power control

	send_data(0xaf, 0); //Display on

	//lcd clear
	clear_lcd();

	//write string
	for(i=0; i<len-1; i++) {
		//upper side of a letter
		set_page(3, i*8+1);
		for(j=0; j<8; j++) {
			send_data(fonts[(int)buffer[i]-32][j], 1);
		}
		//under side of a letter
		set_page(4, i*8+1);
		for(j=8; j<16; j++) {
			send_data(fonts[(int)buffer[i]-32][j], 1);
		}
	}


	return len;
}

static int dev_release(struct inode *ip, struct file *fp)
{
	printk(KERN_INFO "dev_released\n");
	return 0;
}

static struct file_operations fops =
{
	.open = dev_open,
	.write = dev_write,
	.release = dev_release,
};

static int mod_init(void)
{
	major = register_chrdev(0, DEV_NAME, &fops);
	printk(KERN_INFO "mod_init %d\n", major);
	//if(check_mem_region(ADDR, RANGE))
		request_mem_region(ADDR, RANGE, DEV_NAME);
	//else
		//printk(KERN_WARNING "Cannot get I/O region\n");
	addr = ioremap(ADDR, RANGE);

	return 0;
}

static void mod_exit(void)
{
	printk(KERN_INFO "mod_exit\n");
	iounmap(addr);
	release_mem_region(ADDR, RANGE);
	unregister_chrdev(major, DEV_NAME);
}

module_init(mod_init);
module_exit(mod_exit);
