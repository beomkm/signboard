//bcm2385
//base address
//write 0 to gpio : addr+0x28
//write 1 to gpio : addr+0x1c
static int send_data(char value, char type)
{
	int i;
	int temp = value;
	writel(1<<cs, (addr+0x28));
	writel(1<<clk, (addr+0x1c));
	if(type)
		writel(1<<a0, (addr+0x1c));
	else
		writel(1<<a0, (addr+0x28));

	for(i=0; i<8; i++) {
		writel(1<<clk, (addr+0x28));
		if(temp & 0x80)
			writel(1<<si, (addr+0x1c));
		else
			writel(1<<si, (addr+0x28));
		temp = temp << 1;
		writel(1<<clk, (addr+0x1c));
	}

	writel(1<<cs, (addr+0x1c));
	return 0;
}

//1 page = 8px x 8px
static int set_page(int page, int col)
{
	int p = page | 0xb0;
	int lsb = col & 0x0f;
	int msb = col & 0xf0;
	msb = msb >> 4;
	msb = msb | 0x10;
	send_data(p, 0);
	send_data(msb, 0);
	send_data(lsb, 0);
	return 0;
}

static int clear_lcd(void)
{
	int i;
	int j;
	writel(1<<cs, (addr+0x28));
	for(i=0; i<8; i++) {
		set_page(i, 1);
		for(j=0; j<128; j++)
			send_data(0x00, 1);
	}
	writel(1<<cs, (addr+0x1c));

	return 0;
}


